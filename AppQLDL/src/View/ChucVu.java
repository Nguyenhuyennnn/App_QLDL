    private boolean KiemTraDL() throws HeadlessException{
        StringBuilder sb= new StringBuilder();
        if(txtTenCV.getText().equals("")){
            sb.append("VUI LÒNG NHẬP TÊN!\n");
            txtTenCV.setBackground(Color.yellow);
            txtTenCV.requestFocus();
        }
        else{
            txtTenCV.setBackground(Color.white);
            
        }
        if(frmHS.getText().equals("")){
            sb.append("VUI LÒNG NHẬP HỆ SỐ!\n");
            frmHS.setBackground(Color.yellow);
            frmHS.requestFocus();
        }
        else{
            frmHS.setBackground(Color.white);
        }
        if(sb.length()>0){
            JOptionPane.showConfirmDialog(this, sb.toString(),"Thông báo",JOptionPane.ERROR_MESSAGE);
            return false;
        }
        else{
            return true;
        }
        
    }
