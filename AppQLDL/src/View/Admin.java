    private boolean KiemTraDL() throws HeadlessException{
        StringBuilder sb= new StringBuilder();
        
        if(txttenTK.getText().equals("")){
            sb.append("VUI LÒNG NHẬP TÀI KHOẢN!\n");
            txttenTK.setBackground(Color.yellow);
            txttenTK.requestFocus();
        }
        else{
            txttenTK.setBackground(Color.white);
            
        }
        if(txtMK.getText().equals("")){
            sb.append("VUI LÒNG NHẬP MẬT KHẨU!\n");
            txtMK.setBackground(Color.yellow);
            txtMK.requestFocus();
        }
        else{
            txtMK.setBackground(Color.white);
            
        }
        
        if(cbomanv.getSelectedItem().equals("")){
            sb.append("VUI LÒNG CHỌN NHÂN VIÊN!\n");
            cbomanv.setBackground(Color.yellow);
            cbomanv.requestFocus();
        }
        else{
            cbomanv.setBackground(Color.white);
            
        }
        
        if(cboQuyen.getSelectedItem().equals("")){
            sb.append("VUI LÒNG CẤP QUYỀN CHO TÀI KHOẢN!\n");
            cboQuyen.setBackground(Color.yellow);
            cboQuyen.requestFocus();
        }
        else{
            cboQuyen.setBackground(Color.white);
            
        }
        
        if(sb.length()>0){
            JOptionPane.showConfirmDialog(this, sb.toString(),"Thông báo",JOptionPane.ERROR_MESSAGE);
            return false;
        }
        else{
            return true;
        }
    }

