public void getListTinhLuong() throws SQLException {
        TblNhanvien nhanvien = (TblNhanvien) cbomaNV.getSelectedItem();
        String MaNV = String.valueOf(nhanvien.getManv());
        KNCSDL kn = new KNCSDL();
        ArrayList<TblChitietdiemdanh> list = new ArrayList<>();
        Connection conn = kn.getConnect();
        String sql = "SELECT tbl_chitietdiemdanh.*FROM tbl_chitietdiemdanh where month(ngay)=" + cbothang.getSelectedItem() + " AND MaNV=" + MaNV;
        Statement statement = (Statement) conn.createStatement();
        ResultSet result = statement.executeQuery(sql);
        int giovao = 0;
        int giora = 0;
        int tongsogio = 0;
        while (result.next()) {
            TblChitietdiemdanh nv = new TblChitietdiemdanh();
        
            tongsogio++;

            list.add(nv);
        }

        int x = tongsogio;
        txtsoNLV.setText(x + "");

    }
